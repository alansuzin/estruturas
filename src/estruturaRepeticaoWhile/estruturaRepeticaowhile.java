package estruturaRepeticaoWhile;


public class estruturaRepeticaowhile {
        public static void main(String[] args) {
        
            //Esstrutura de Repetição While
            
            //Inicio do Laço de Repetição
            int i = 0;
            
            //Fim do Laço de Repetição
            while(i <= 10){
                
                System.out.println("Número de Repetições: " + i);
                
                //Incremento
                i++;
            }
            
            
            
    }
}
